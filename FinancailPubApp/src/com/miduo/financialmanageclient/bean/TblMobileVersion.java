package com.miduo.financialmanageclient.bean;

import java.io.Serializable;

public class TblMobileVersion implements Serializable {
	private static final long serialVersionUID = -6529400814238932008L;
	private Integer id;
	private String mobileName;
	private String mobileVersion;
	private String updateUrl;
	private String updateMsg;
	// 0无更新 1非强制更新 2强制更新
	private Integer updateFlag;
	private String storePlatform;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getMobileName() {
		return mobileName;
	}

	public void setMobileName(String mobileName) {
		this.mobileName = mobileName;
	}

	public String getMobileVersion() {
		return mobileVersion;
	}

	public void setMobileVersion(String mobileVersion) {
		this.mobileVersion = mobileVersion;
	}

	public String getUpdateUrl() {
		return updateUrl;
	}

	public void setUpdateUrl(String updateUrl) {
		this.updateUrl = updateUrl;
	}

	public String getUpdateMsg() {
		return updateMsg;
	}

	public void setUpdateMsg(String updateMsg) {
		this.updateMsg = updateMsg;
	}

	public Integer getUpdateFlag() {
		return updateFlag;
	}

	public void setUpdateFlag(Integer updateFlag) {
		this.updateFlag = updateFlag;
	}

	public String getStorePlatform() {
		return storePlatform;
	}

	public void setStorePlatform(String storePlatform) {
		this.storePlatform = storePlatform;
	}

}
