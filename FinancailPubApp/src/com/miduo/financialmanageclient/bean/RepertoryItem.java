package com.miduo.financialmanageclient.bean;

import java.util.List;

public class RepertoryItem {
	private Integer pcategoryId;
	private String pcategoryName;
	private Integer assetCount;
	private double assetAmount;
	private List<UserAssetInfoForAppVo> userAssetList;//资产列表
	private boolean isExtend = false;
	public Integer getPcategoryId() {
		return pcategoryId;
	}
	public void setPcategoryId(Integer pcategoryId) {
		this.pcategoryId = pcategoryId;
	}
	public String getPcategoryName() {
		return pcategoryName;
	}
	public void setPcategoryName(String pcategoryName) {
		this.pcategoryName = pcategoryName;
	}
	public Integer getAssetCount() {
		return assetCount;
	}
	public void setAssetCount(Integer assetCount) {
		this.assetCount = assetCount;
	}
	public double getAssetAmount() {
		return assetAmount;
	}
	public void setAssetAmount(double assetAmount) {
		this.assetAmount = assetAmount;
	}	
	public boolean isExtend() {
		return isExtend;
	}
	public void setExtend(boolean isExtend) {
		this.isExtend = isExtend;
	}
	public List<UserAssetInfoForAppVo> getUserAssetList() {
		return userAssetList;
	}
	public void setUserAssetList(List<UserAssetInfoForAppVo> userAssetList) {
		this.userAssetList = userAssetList;
	}
	@Override
	public String toString() {
		return "RepertoryItem [pcategoryId=" + pcategoryId + ", pcategoryName=" + pcategoryName + ", assetCount="
				+ assetCount + ", assetAmount=" + assetAmount + ", userAssetList=" + userAssetList + ", isExtend="
				+ isExtend + "]";
	}
	
	
}
