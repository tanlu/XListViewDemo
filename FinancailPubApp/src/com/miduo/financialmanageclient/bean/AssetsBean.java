package com.miduo.financialmanageclient.bean;

import java.io.Serializable;

public class AssetsBean implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	/**
	 * 米多资产列表使用
	 */
	private String status;
	private long time;
	private int type;//
	private  int isfixedincome;

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public long getTime() {
		return time;
	}

	public void setTime(long time) {
		this.time = time;
	}
	

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}
	
	

	public int getIsfixedincome() {
		return isfixedincome;
	}

	public void setIsfixedincome(int isfixedincome) {
		this.isfixedincome = isfixedincome;
	}

	@Override
	public String toString() {
		return "AssetsBean [status=" + status + ", time=" + time + "]";
	}

	public AssetsBean(String status, long time) {
		super();
		this.status = status;
		this.time = time;
	}

	public AssetsBean(String status, long time, int type) {
		super();
		this.status = status;
		this.time = time;
		this.type = type;
	}

	public AssetsBean(String status, long time, int type, int isfixedincome) {
		super();
		this.status = status;
		this.time = time;
		this.type = type;
		this.isfixedincome = isfixedincome;
	}
	
	
	
	
	
	
	

}
