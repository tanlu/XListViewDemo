package com.miduo.financialmanageclient.bean;

import java.io.Serializable;

public class UserTradeRecordsVo implements Serializable {
	/**
	 * 交易日期
	 */
	private String tradeDate;
	/**
	 * 产品名称
	 */
	private String productName;
	/**
	 * 交易类型
	 */
	private Integer tradeType;
	/**
	 * 交易类型名称
	 */
	private String tradeTypeName;
	/**
	 * 交易金额
	 */
	private Double tradeAmount;
	/**
	 * 交易状态
	 */
	private Integer tradeState;
	/**
	 * 交易状态名称
	 */
	private String tradeStateName;

	public String getTradeDate() {
		return tradeDate;
	}

	public void setTradeDate(String tradeDate) {
		this.tradeDate = tradeDate;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public Integer getTradeType() {
		return tradeType;
	}

	public void setTradeType(Integer tradeType) {
		this.tradeType = tradeType;
	}

	public String getTradeTypeName() {
		return tradeTypeName;
	}

	public void setTradeTypeName(String tradeTypeName) {
		this.tradeTypeName = tradeTypeName;
	}

	public Double getTradeAmount() {
		return tradeAmount;
	}

	public void setTradeAmount(Double tradeAmount) {
		this.tradeAmount = tradeAmount;
	}

	public Integer getTradeState() {
		return tradeState;
	}

	public void setTradeState(Integer tradeState) {
		this.tradeState = tradeState;
	}

	public String getTradeStateName() {
		return tradeStateName;
	}

	public void setTradeStateName(String tradeStateName) {
		this.tradeStateName = tradeStateName;
	}

	@Override
	public String toString() {
		return "UserTradeRecordsVo [tradeDate=" + tradeDate + ", productName=" + productName + ", tradeType="
				+ tradeType + ", tradeTypeName=" + tradeTypeName + ", tradeAmount=" + tradeAmount + ", tradeState="
				+ tradeState + ", tradeStateName=" + tradeStateName + "]";
	}

}
