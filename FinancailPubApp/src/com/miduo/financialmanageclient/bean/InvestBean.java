package com.miduo.financialmanageclient.bean;

import java.math.BigDecimal;

public class InvestBean {
	private Integer age;
	private BigDecimal aftertaxIncome;
	private BigDecimal investmentFunds;
	private Integer carefor;
	private Integer howDo;

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

	public BigDecimal getAftertaxIncome() {
		return aftertaxIncome;
	}

	public void setAftertaxIncome(BigDecimal aftertaxIncome) {
		this.aftertaxIncome = aftertaxIncome;
	}

	public BigDecimal getInvestmentFunds() {
		return investmentFunds;
	}

	public void setInvestmentFunds(BigDecimal investmentFunds) {
		this.investmentFunds = investmentFunds;
	}

	public Integer getCarefor() {
		return carefor;
	}

	public void setCarefor(Integer carefor) {
		this.carefor = carefor;
	}

	public Integer getHowDo() {
		return howDo;
	}

	public void setHowDo(Integer howDo) {
		this.howDo = howDo;
	}

	@Override
	public String toString() {
		return "InvestBean [age=" + age + ", aftertaxIncome=" + aftertaxIncome
				+ ", investmentFunds=" + investmentFunds + ", carefor="
				+ carefor + ", howDo=" + howDo + "]";
	}

}
